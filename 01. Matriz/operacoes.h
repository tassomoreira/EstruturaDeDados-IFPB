/*Operações TAD Matriz*/

/* Função Produto
* Realiza o produto de duas matrizes informadas e retorna uma matriz com o resultado
*/
Matriz matrizProduto(Matriz matriz1, Matriz matriz2);

/* Função Somar
* Realiza a soma de duas matriz informadas e retorna uma matriz com o resultado
*/
Matriz matrizSomar(Matriz matriz1, Matriz matriz2);

/* Função Transposta
* Transpoe uma matriz e retorna uma matriz com o resultado
*/
Matriz matrizTransposta(Matriz matriz);

/* Função TranspostaSoma
* Imprime a transposta da soma de duas matrizes
*/
void matrizTranspostaSoma(Matriz matriz1, Matriz matriz2);

/* Função Identidade
* Verifica se uma matriz eh uma matriz identidade
*/
int matrizIdentidade(Matriz matriz);

/* Função Inversa
* Verifica se uma matriz eh a inversa da outra
*/
int matrizInversa(Matriz matriz1, Matriz matriz2);